package virtualpetshelter;

public class RobotCat extends RobotPet {


    public RobotCat( String name, String description, String  type, int boredom, int health, int oilLevel, int happiness) {
        super(name, description, type, boredom, health, oilLevel, happiness);
        this.name = name;
        this.description = description;
        this.type = type;
        this.boredom = boredom;
        this.health = health;
        this.happiness = happiness;
        this.oilLevel = oilLevel;

    }







}
